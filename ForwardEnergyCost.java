public class ForwardEnergyCost {
     double cu;
     double cl;
     double cr;

    public ForwardEnergyCost(double cu, double cl, double cr) {
        this.cu = cu;
        this.cl = cl;
        this.cr = cr;
    }

    public double getCu() {
        return cu;
    }

    public double getCl() {
        return cl;
    }

    public double getCr() {
        return cr;
    }
}
